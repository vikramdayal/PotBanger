    private void loadSongs() {
        listSongsModel = (DefaultListModel)listSongs.getModel();
        try (Stream<String> stream = Files.lines(Paths.get("songs.vickyd"))) {
            stream.forEach(s ->listSongsModel.addElement(s));
        } catch (IOException ex) {
            //Logger.getLogger(StartScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        //listSongs = new javax.swing.JList(listSongsModel);
    }
    
    private void saveSongs(){
        PrintWriter out = null;
        try {
            //BuffereWiter pf = new BuffereWiter(new File("songs.vickyd"));
            out = new PrintWriter(new FileWriter("songs.vickyd"));
            for (Object s: listSongsModel.toArray()){
                out.println((String)s);
            }
        } catch (IOException ex) {
            Logger.getLogger(StartScreen.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            if (out != null)
                out.close();
        }
    }

