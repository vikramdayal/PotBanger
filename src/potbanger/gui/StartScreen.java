    private void btnLoadExistingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadExistingActionPerformed
        int inx = listSongs.getSelectedIndex();
        if (inx == -1){
            JOptionPane.showMessageDialog(this, "Select a song first.");
            return;
        }
        String name= (String)listSongsModel.elementAt(inx);
        PlaySheet = potbanger.play.Sheet.load(name);
        if(PlaySheet==null)
            PlaySheet = new potbanger.play.Sheet(name);
        runSheetScreen();
    }//GEN-LAST:event_btnLoadExistingActionPerformed

    private void btnPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlayActionPerformed
        int inx = listSongs.getSelectedIndex();
        if (inx == -1){
            JOptionPane.showMessageDialog(this, "Select a song first.");
            return;
        }
        potbanger.play.Sheet.init();
        String name= (String)listSongsModel.elementAt(inx);
        PlaySheet = potbanger.play.Sheet.load(name);
        if(PlaySheet==null) {
            JOptionPane.showMessageDialog(this, "Error in playsheet, edit it first..");
            return;            
        }
        PlaySheet.play();
    }//GEN-LAST:event_btnPlayActionPerformed

